const int n = 3 * 1e5;
vector <vector<int>> graph1(n), graph2(n);
vector <int> vis(n), store(n);

void bfs(int root) {
	vis[root] = 1;

	queue <int> q;
	q.push(root);

	while (!q.empty()) {
		int curr = q.front();
		q.pop();
		for (int child : graph1[curr]) {
			if (vis[child]) continue;
			q.push(child); vis[child] = 1;
			store[child] = 1 + store[curr];
		}
	}
}


vector <int> vis(1e5), level(1e5);

bool isValid(int n) {
	return n > 0 && n < 1e5;
}

int bfs(int root, int dest) {
	queue <int> q;
	q.push(root);
	vis[root] = 1;

	while (!q.empty()) {
		int temp = q.front();
		q.pop();

		if (isValid(temp - 1) && !vis[temp - 1]) {
			q.push(temp - 1);
			level[temp - 1] = 1 + level[temp];
			vis[temp - 1] = 1;
		}

		if (isValid(temp * 2) && !vis[2 * temp]) {
			q.push(2 * temp);
			level[2 * temp] = 1 + level[temp];
			vis[2 * temp] = 1;
		}
	} return level[dest];
}



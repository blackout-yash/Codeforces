int solve(int n, int a, int b, int c, vector <int> &dp) {
	if (n == 0) return 0;
	else if (dp[n] != -2) return dp[n];

	vector <int> arr(3, -1);
	if (n >= a) arr[0] = solve(n - a, a, b, c, dp);
	if (n >= b) arr[1] = solve(n - b, a, b, c, dp);
	if (n >= c) arr[2] = solve(n - c, a, b, c, dp);

	int maximum = -1;
	for (auto x : arr) {
		if (x != -1) maximum = max(x, maximum);
	}
	if (maximum != -1) return dp[n] = maximum + 1;
	return dp[n] = -1;
}


